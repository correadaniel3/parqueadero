package co.ceiba.parqueadero.utils;

public class Tarifa {
	
	private double horaCarro=1000;
	private double horaMoto=500;
	private double diaCarro=8000;
	private double diaMoto=4000;
	
	public Tarifa() {}

	public double getHoraCarro() {
		return horaCarro;
	}

	public double getHoraMoto() {
		return horaMoto;
	}

	public double getDiaCarro() {
		return diaCarro;
	}

	public double getDiaMoto() {
		return diaMoto;
	}
	
	

}
